    private void btnBillActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBillActionPerformed
        // TODO add your handling code here:
        User user = null;
        user = UserDao.getName(userEmail);
        if(user != null){
            userName = user.getName();
        }
        String cusName = txtCusName.getText();
        String cusPhone = txtCusPhone.getText();
        String cusEmail = txtEmail.getText();
        SimpleDateFormat formater = new SimpleDateFormat("dd-MM-yyyy");
        Date date = new Date();
        String toDay = formater.format(date);
        String costTotal = String.valueOf(cost);
        String createdBy = userEmail;
        Bill bill = new Bill();
        bill.setName(cusName);
        bill.setPhoneNumber(cusPhone);
        bill.setEmail(cusEmail);
        bill.setDate(toDay);
        bill.setCost(costTotal);
        bill.setCreatedBy(createdBy);
        BillDao.save(bill);
        String path = "D:\\Code\\Java\\Quan Ly Cake Project\\BILL\\";
        com.itextpdf.text.Document doc = new com.itextpdf.text.Document();
        try{
            PdfWriter.getInstance(doc, new FileOutputStream(path + "" + billid + ".pdf" ));
            doc.open();
            Paragraph cakeName = new Paragraph("Welcome to our Cake Shop\n");
            doc.add(cakeName);
            Paragraph startLine = new Paragraph("********************************************************");
            doc.add(startLine);
            Paragraph paragraph = new Paragraph("\tBill ID: " + billid + "\nStaff: " + userName  +"\nCustomer Name: "+cusName+ "\nCost of bill: " +cost + "$" + "\nTime: " + toDay+ "\n\n\n");
            doc.add(paragraph);
            PdfPTable tb =  new PdfPTable(4);
            tb.addCell("Name");
            tb.addCell("Price");
            tb.addCell("Quantity");
            tb.addCell("Cost");
            for(int i = 0; i < tableDetail.getRowCount(); i++){
                String name = tableDetail.getValueAt(i, 0).toString();
                String price = tableDetail.getValueAt(i, 1).toString();
                String quantity = tableDetail.getValueAt(i, 2).toString();
                String costBill = tableDetail.getValueAt(i, 3).toString();
                tb.addCell(name);
                tb.addCell(price);
                tb.addCell(quantity);
                tb.addCell(costBill);
            }
            doc.add(tb); 
            doc.add(startLine);
            Paragraph paragraph1 = new Paragraph("\nThank you very much \nWifi: cakebakeryUIT");
            doc.add(paragraph1);
            //Open pdf
            OpenPdf.openById(String.valueOf(billid));
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
        doc.close();
        setVisible(false);
        new PlaceOrder(createdBy).setVisible(true);
    }//GEN-LAST:event_btnBillActionPerformed

    private void tableDetailMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableDetailMouseClicked
        // TODO add your handling code here:
        int index = tableDetail.getSelectedRow();
        int a = JOptionPane.showConfirmDialog(null, "Do you want to delete this product out of cart", "Select", JOptionPane.YES_NO_OPTION);
        if(a == 0){
            TableModel model = tableDetail.getModel();
            String total = model.getValueAt(index, 3).toString();
            cost = cost - Integer.parseInt(total);
            txtCost.setText(String.valueOf(cost));
            ((DefaultTableModel) tableDetail.getModel()).removeRow(index);
           
        }
    }//GEN-LAST:event_tableDetailMouseClicked

