    public void productNameByCategory(String category){
        DefaultTableModel dtm =(DefaultTableModel) tableProduct.getModel();
        dtm.setRowCount(0);
        ArrayList<Product> listProduct = ProductDao.getProductByCategory(category);
        Iterator<Product> itr = listProduct.iterator();
        while(itr.hasNext()){
            Product productObj = itr.next();
            dtm.addRow(new Object[]{productObj.getName()});
        }
    }

