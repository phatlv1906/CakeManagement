    public void validateFields(){
        String customerName = txtCusName.getText();
        String customerPhone = txtCusPhone.getText();
        String customerEmail = txtEmail.getText();
        if(!customerEmail.equals("") && customerPhone.matches(phoneNumberPattern) && customerEmail.matches(emailPattern) && customerPhone.length() == 10 && cost > 0){
            btnBill.setEnabled(true);
        }else {
            btnBill.setEnabled(false);
            
        }
    }
 
